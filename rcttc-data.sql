insert into theater (`name`, address_id, phone_id, email_id)
    select
		distinct ed.theater,
        a.address_id,
        p.phone_id,
        e.email_id
	from excel_data ed
    inner join address a on a.home_address = ed.theater_address
    inner join phone p on p.phone_number = ed.theater_phone
    inner join email e on e.email_address = ed.theater_email;
  
insert into seat (seat_name, theater_id)
	select 
		distinct ed.seat,
        t.theater_id
	from excel_data ed
    inner join theater t on t.`name` = ed.theater
	order by t.theater_id, ed.seat;
    
insert into customer (first_name, last_name, email_id, phone_id, address_id)
	select
		distinct ed.customer_first,
        ed.customer_last,
        e.email_id,
        p.phone_id,
        a.address_id
	from excel_data ed
    inner join email e on e.email_address = ed.customer_email
    inner join phone p on p.phone_number = ed.customer_phone
    inner join address a on a.home_address = ed.customer_address;
    
